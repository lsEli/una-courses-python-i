# Ejemplo 1: Entero
print(type(42))  # <class 'int'>

# Ejemplo 2: String
print(type("Hola"))  # <class 'str'>

# Ejemplo 3: Lista
print(type([1, 2, 3]))  # <class 'list'>

# Ejemplo 4: Variable evaluada
price = 19.99
print(type(price))  # <class 'float'>

# Ejemplo 5: Diccionario
data = {"name": "Luis", "age": 28}
print(type(data))  # <class 'dict'>
